@app.cell
def _(script):
    import re
    import itertools


    PATTERN = r"^[a-zA-Z\s\(\)]+:\s*(.*)$"


    def clean_line(line: str) -> str:
        return line.strip()


    def is_quote(line: str) -> bool:
        return re.compile(PATTERN).search(line)


    def extract_quote(line: str) -> str:
        try:
            return re.compile(PATTERN).match(line).group(1)
        except:
            raise Exception(line)


    def count_words(line: str) -> int:
        return len(line.split(" "))


    lines = script.split("\n")

    word_count = list(
        itertools.accumulate(
            map(
                count_words,
                map(
                    extract_quote,
                    filter(is_quote, filter(None, map(clean_line, lines))),
                ),
            )
        )
    )
    word_count[-10:]
    return


